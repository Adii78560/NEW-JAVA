package Introductions;

public class stars {
    public static void main(String[] args){
        //simple star
        int n=6;
        for(int i=0;i<n;i++){
            System.out.print("*");
        }
        //rows and columns
        System.out.println("");
        System.out.println("Rows and Colomns");
        for(int i=0;i<n;i++){
            for(int j=0;j<n;j++){
                System.out.print("*");
            }
            System.out.println("");

        }

        


    }
}
